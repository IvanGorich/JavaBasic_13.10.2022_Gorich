public class Parallelepiped {
    public static void main(String[] args) {
        int a = 3;
        int b = 5;
        int c = 7;

        int volume = a * b * c;
        System.out.println("Parallelepiped volume = " + volume);
        System.out.println();

        int length = a * 4 + b * 4 + c * 4;
        System.out.println("Total Length = " + length);

    }
}
